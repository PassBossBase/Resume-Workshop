type Translations = typeof translations;
export type LocaleMessages = Translations[AppLocale];

interface LocaleContextValue {
  locale: AppLocale;
  setLocale: (locale: AppLocale) => void;
  toggleLocale: () => void;
  t: LocaleMessages;
}

const LocaleContext = createContext<LocaleContextValue | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const locale = useSyncExternalStore(
    subscribeToLocale,
    getLocaleSnapshot,
    getServerLocaleSnapshot,
  );

  useEffect(() => {
    document.documentElement.lang = locale;
    clearPendingLocaleVisibility(locale);
  }, [locale]);

  const setLocale = useCallback((nextLocale: AppLocale) => {
    window.localStorage.setItem(localeStorageKey, nextLocale);
    document.documentElement.dataset.initialLocale = nextLocale;
    window.dispatchEvent(new Event(localeChangeEvent));
  }, []);

  const value = useMemo<LocaleContextValue>(
    () => ({
      locale,
      setLocale,
      t: translations[locale],
      toggleLocale: () => setLocale(getOppositeLocale(locale)),
    }),
    [locale, setLocale],
  );

  return (
    <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
  );
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error("useLocale must be used inside LocaleProvider");
  }
  return context;
}

export function useT() {
  return useLocale().t;
}

export function formatLocaleDateTime(value: string, locale: AppLocale): string {
  return new Date(value).toLocaleString(locale);
}
